export function translate(
  locale: Locale,
  key: TranslationKey,
  values: Record<string, string | number> = {}
) {
  return dictionaries[locale][key].replace(/\{\{(\w+)\}\}/g, (_, token: string) =>
    String(values[token] ?? "")
  );
}

